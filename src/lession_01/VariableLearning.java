package lession_01;

public class VariableLearning {
    public static void main(String[] args) {
        // snake_case, camelCase , PascalCase
    int myIntNumber = 150;
    System.out.println(myIntNumber);
    System.out.println(Byte.MAX_VALUE);
    System.out.println(Integer.MAX_VALUE);


    float myFloatNumber= 2.1F;
    System.out.println(myFloatNumber);
    System.out.println(Float.MAX_VALUE);

    long myLongnumber = 42424L;
    double myDoublenumber = 2.1D;
    System.out.println(myLongnumber);
    System.out.println(Long.MAX_VALUE);
    System.out.println(myDoublenumber);


    }
}

